<h1>HDW Player, An Excellent Video Player component for Joomla!</h1>
HDW Player is a powerful Joomla Video Player component of <a target="_blank" href="http://www.hdwplayer.com/index.php/joomla-video-gallery">http://hdwplayer.com</a>. The Player is built in Adobe Flash. It supports playback of any format that Adobe Flash Player can handle such as flv, mp4, 3g2, 3gp, aac, f4b, f4p, f4v, m4a, m4v, mov(h.264), sdp, vp6, smil etc...<br />
<h3>Is the Component Free?</h3>
Com_hdwplayer is released under the <a target="_blank" href="http://www.gnu.org/licenses/gpl-2.0.html">GNU/GPL v2</a> license, but the player comes in two versions: commercial and non-commercial. The commercial version allow users to brand the player with their own logo and copyright, while the non-commercial player does not.
<h3>Support For the Player?</h3>
Com_hdwplayer has a separate support team for the player with two avenues of support: e-mail (support@hdwplayer.com) and forum. Users can get instant support through forum anytime Monday through Friday. If users are not satisfied with the support, they can complain directly to admin@hdwplayer.com.
<h3>Where Should I Complain about a Bug in the Component?</h3>
If you find a bug, just e-mail support@hdwplayer.com. The team will fix it and update the component immediately.
<h3>Recommend new Options for HDW Player and the Component!</h3>
HDW Player is constantly eager to add new functionalities to its player. If you have suggestions that might make the component & player shine even brighter, let us know by e-mailing them to admin@hdwplayer. We will review your recommendations and try to implement them in our component.